function Random() {
    let Random = Math.random();
    return Random
}

function Root(Number) {
    let Root = Math.sqrt(Number);
    return Root
}

function Round(NumberOne, NumberTwo) {
    Round = Math.round(NumberOne, NumberTwo);
    return Round
}

function Exponent(Number, Power) {
    let Exponent = Math.pow(Number, Power);
    return Exponent
}

function Truncate(Float) {
    let Trunc = Math.trunc(Float);
    return Trunc
}